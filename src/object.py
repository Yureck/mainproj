"""
Демонстрация преобразований и методов целых чисел.

Включает:
- Свойства чисел (.real, .imag)
- Преобразование типов (int, float, complex)
- Двоичное, восьмеричное, шестнадцатеричное представление
- Полезные функции для работы с числами (abs, bit_length, divmod)
"""

age = 8

# ========== СВОЙСТВА ЧИСЛА ==========

# .real - вещественная часть (для целых чисел это само число)
print(age.real)  # Output: 8

# .imag - мнимая часть (для целых чисел это 0)
print(age.imag)  # Output: 0


# ========== ПРЕОБРАЗОВАНИЕ ТИПОВ ==========

# int() - преобразование в целое число
print(int(age))  # Output: 8

# float() - преобразование в число с плавающей точкой
print(float(age))  # Output: 8.0

# complex() - преобразование в комплексное число (a + 0j)
print(complex(age))  # Output: (8+0j)


# ========== СИСТЕМЫ СЧИСЛЕНИЯ ==========

# bin() - двоичное представление (Binary)
# Префикс 0b означает, что это двоичное число
print(bin(age))  # Output: 0b1000 (8 в двоичной системе)

# oct() - восьмеричное представление (Octal)
# Префикс 0o означает восьмеричное число
print(oct(age))  # Output: 0o10 (8 в восьмеричной системе)

# hex() - шестнадцатеричное представление (Hexadecimal)
# Префикс 0x означает шестнадцатеричное число
print(hex(age))  # Output: 0x8 (8 в шестнадцатеричной системе)


# ========== МАТЕМАТИЧЕСКИЕ ФУНКЦИИ ==========

# abs() - абсолютное значение (модуль)
print(abs(-age))  # Output: 8

# .bit_length() - количество битов для представления числа
# Для 8 (0b1000) нужно 4 бита
print(age.bit_length())  # Output: 4

# divmod(делимое, делитель) - возвращает кортеж (частное, остаток)
# 20 / 3 = 6 (остаток 2)
print(divmod(20, 3))  # Output: (6, 2)  
