age = 8
print(age.real)  # Output: 8
print(age.imag)  # Output: 0
print(int(age))   # Output: 8
print(float(age)) # Output: 8.0 
print(complex(age))  # Output: (8+0j)
print(bin(age))   # Output: 0b1000
print(oct(age))   # Output: 0o10
print(hex(age))   # Output: 0x8
print(abs(-age))  # Output: 8
print(age.bit_length())  # Output: 4
print(divmod(20, 3))  # Output: (6, 2)  
