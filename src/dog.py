"""
Демонстрация импорта модулей и классов.

Показывает различные способы импорта:
- import модуль (импортирует весь модуль)
- from модуль import класс (импортирует конкретный класс)
- Встроенные модули (math)
"""

# Способ 1: импортируем весь модуль classess
import classess

# Получаем доступ к классу Dog через модуль
# dog_instance = classess.Dog("Rex", 5)


# Способ 2: импортируем конкретный класс Dog из модуля classess
from classess import Dog

# Теперь можно использовать Dog напрямую
# dog_instance = Dog("Rex", 5)


# Встроенный модуль math содержит математические функции и константы
