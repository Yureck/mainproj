"""
Полный гайд по работе со МНОЖЕСТВАМИ (Set) в Python.
Множества - это неупорядоченные коллекции уникальных элементов.

Основные операции:
- Пересечение (&)
- Объединение (|)
- Разность (-)
- Симметричная разность (^)
"""

# Создание множеств
set1 = {"Alice", "Bob", "Charlie", "Diana"}
set2 = {"Charlie", "Diana", "Eve", "Frank"}


# ========== ПЕРЕСЕЧЕНИЕ (Intersection) ==========
# & - операция пересечения, возвращает элементы, общие для обоих множеств
interset = set1 & set2
print(interset)  # Output: {'Charlie', 'Diana'}


# ========== ОБЪЕДИНЕНИЕ (Union) ==========
# | - операция объединения, возвращает все элементы из обоих множеств (без дубликатов)
mod = set1 | set2
print(mod)  # Output: {'Alice', 'Bob', 'Charlie', 'Diana', 'Eve', 'Frank'}


# ========== РАЗНОСТЬ (Difference) ==========
# - - операция разности, возвращает элементы из первого множества, которых нет во втором
diff = set1 - set2
print(diff)  # Output: {'Alice', 'Bob'}


# ========== СИММЕТРИЧНАЯ РАЗНОСТЬ (Symmetric Difference) ==========
# ^ - симметричная разность, возвращает элементы, которые есть в одном множестве, но не в обоих
symdiff = set1 ^ set2
print(symdiff)  # Output: {'Alice', 'Bob', 'Eve', 'Frank'}


# ========== ПРЕОБРАЗОВАНИЕ В СПИСОК ==========
# list() - преобразует множество в список (порядок не гарантирован)
print(list(set1))  # Output: ['Alice', 'Bob', 'Charlie', 'Diana'] (порядок может быть другой)